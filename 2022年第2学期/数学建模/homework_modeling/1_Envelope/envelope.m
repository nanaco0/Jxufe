x=(0:pi/100:2*pi)';
%步距为pi/100,范围0~2*pi。
y1=2*exp(-0.5*x)*[1,-1];
%矩阵乘法
y2=2*exp(-0.5*x).*sin(2*pi*x);
%设曲线函数
x1=(0:12)/2;
%离散数据点横坐标
y3=2*exp(-0.5*x1).*sin(2*pi*x1);
%离散数据点纵坐标
plot(x,y1,'g:',x,y2,'b--',x1,y3,'rp');
%连点，设置颜色、线型
title('曲线及其包络线');
%加图形标题
legend('包络线1','包络线2','曲线y','离散数据点');  
%加图例
text(2.8,0.5,'包络线1');
%添加标签说明
text(2.8,-0.5,'包络线2');
text(0.5,0.5,'曲线y');
text(1.4,0.1,'离散数据点');
xlabel('independentvariableX');
%加X轴说明
ylabel('independentvariableY');
%加Y轴说明